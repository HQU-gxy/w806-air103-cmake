#ifndef INT_ALIAS_H
#define INT_ALIAS_H
#include <stdint.h>

#ifndef u8
typedef uint8_t u8;
#endif
#ifndef u16
typedef uint16_t u16;
#endif
#ifndef u32
typedef uint32_t u32;
#endif
#ifndef u64
typedef uint64_t u64;
#endif
#ifndef s8
typedef int8_t s8;
#endif
#ifndef s16
typedef int16_t s16;
#endif
#ifndef s32
typedef int32_t s32;
#endif
#ifndef s64
typedef int64_t s64;
#endif

#ifndef u_char
typedef unsigned char u_char;
#endif

#ifndef INT8U
typedef unsigned char INT8U;
#endif

#ifndef INT8S
typedef signed char INT8S;
#endif
#endif //INT_ALIAS_H
